"""Write a Python program to sum of three given integers. However, if
two values are equal sum will be zero."""


a=int(input("Enter 1st value :"))
b=int(input("Enter 2nd value "))
c=int(input("Enter 3rd value :"))

if a==b:
    print(a-b)
elif b==c:
    print(b-c)
elif c-a:
    print(c-a)
else:
    print(a+b+c)